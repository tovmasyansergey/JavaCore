package classwork.classwork10;

public class SwitchCase {
    public static void main(String[] args) {
        int a = 5;
        switch (a) {
            case 1:
                System.out.println("a havasar e 1");
                break;
            case 2:
                System.out.println("a havasar e 2");
                break;
            case 3:
                System.out.println("a havasar e 3");
            default:
                System.out.println("a havasar che 1,2,3");
        }
    }
}
